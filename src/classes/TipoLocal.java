package classes;

public class TipoLocal {
	private Integer idTipoLocal;
	private String descricaoTipoLocal;

	public TipoLocal() {
	}

	public TipoLocal(Integer idTipoLocal, String descricaoTipoLocal) {
		this.idTipoLocal = idTipoLocal;
		this.descricaoTipoLocal = descricaoTipoLocal;
	}

	public Integer getIdTipoLocal() {
		return idTipoLocal;
	}

	public void setIdTipoLocal(Integer idTipoLocal) {
		this.idTipoLocal = idTipoLocal;
	}

	public String getDescricaoTipoLocal() {
		return descricaoTipoLocal;
	}

	public void setDescricaoTipoLocal(String descricaoTipoLocal) {
		this.descricaoTipoLocal = descricaoTipoLocal;
	}

}
